				<br><br> Os desafios dividem-se em duas categorias: uma de Case Study e outra de Team Design. Em ambas, os participantes têm a oportunidade de pôr em prática os seus conhecimentos, resolvendo problemas ou mesmo criando protótipos, de carácter real.
